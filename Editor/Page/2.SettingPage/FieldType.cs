/// <summary>
/// 字段类型
/// </summary>
public enum FieldType
{
    /// <summary>
    /// 文本框(不可编辑)
    /// </summary>
    TextField,
    /// <summary>
    /// 文本框(可编辑)
    /// </summary>
    EditField,
    /// <summary>
    /// 选择文件夹
    /// </summary>
    Folder,
    /// <summary>
    /// 选择文件
    /// </summary>
    File,
    /// <summary>
    /// Toggle
    /// </summary>
    Toggle,
}